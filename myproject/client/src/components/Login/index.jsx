import React from "react";
import { useState } from "react";
import axios from "axios";
import { Link, useNavigate  } from "react-router-dom";
import styles from "./styles.module.css";

const Login = () => {
	const [data, setData] = useState({ email: "", password: "" });
	const [error, setError] = useState("");
	const navigate = useNavigate();

	const handleChange = ({ currentTarget: input }) => {
		setData({ ...data, [input.name]: input.value });
	};

	const handleSubmit = async (e) => {
		e.preventDefault();
		try {
			const url = "http://localhost:8080/api/auth/login";
			const { data: res } = await axios.post(url, data);
			console.log("Server Response:", res); // ðŸ› ï¸ Debugging line

        if (!res.user) {
            console.error("User data is missing in response!");
            return;
        }
			localStorage.setItem("token", res.token);
			localStorage.setItem("userId", res.user.id);
			localStorage.setItem("isAdmin", res.user.isAdmin);
			if (res.user.surveyCompleted) {
				navigate("/main");
			  } else {
				navigate("/survey");
			  }
		} catch (error) {
			if (
				error.response &&
				error.response.status >= 400 &&
				error.response.status <= 500
			) {
				setError(error.response.data.message);
			}
		}
	};

	return (
		<div className={styles.login_container} style={{ backgroundColor: "#e0f2fe " }}>
			<div className={styles.login_form_container}>
				<div className={styles.left}>
					<form className={styles.form_container} onSubmit={handleSubmit}>
						<h1>Login to Your Account</h1><br></br>
						<input
							type="email"
							placeholder="Email"
							name="email"
							onChange={handleChange}
							value={data.email}
							required
							className={styles.input}
							autoComplete="email"
						/>
						<input
							type="password"
							placeholder="Password"
							name="password"
							onChange={handleChange}
							value={data.password}
							required
							className={styles.input}
							autoComplete="current-password"
						/>
						{error && <div className={styles.error_msg}>{error}</div>}
						<button type="submit" className={styles.green_btn}>
							Sign In
						</button>
					</form>
				</div>
				<div className={styles.right}>
					<h1>New Here ?</h1><br></br>
					<Link to="/signup">
						<button type="button" className={styles.white_btn}>
							Sign Up
						</button>
					</Link>
				</div>
			</div>
		</div>
	);
};

export default Login;